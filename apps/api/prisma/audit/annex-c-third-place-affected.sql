-- CONSULTA 2 — O bracket OFICIAL (tabela matches) viola o Anexo C?
-- 1 linha por confronto; ok = false marca o slot fora do regulamento.
-- ============================================================================
WITH official_assign AS (
  SELECT m.bracket_fixture_id AS fixture_id,
         ht.group_letter      AS winner_group,
         at.group_letter      AS assigned_third_group
  FROM matches m
  JOIN teams ht ON ht.id = m.home_team_id
  JOIN teams at ON at.id = m.away_team_id
  WHERE m.bracket_fixture_id IN
        ('R32-74','R32-77','R32-79','R32-80','R32-81','R32-82','R32-85','R32-87')
),
official_key AS (
  SELECT string_agg(assigned_third_group, '' ORDER BY assigned_third_group) AS combo_key
  FROM official_assign
  HAVING count(*) = 8
)
SELECT oa.fixture_id, oa.winner_group,
       oa.assigned_third_group AS bracket_oficial_pos_3,
       ac.correct_third_group  AS anexo_c_exige_3,
       (oa.assigned_third_group = ac.correct_third_group) AS ok
FROM official_assign oa
CROSS JOIN official_key ok
JOIN annexc ac ON ac.combo_key = ok.combo_key AND ac.winner_group = oa.winner_group
ORDER BY oa.fixture_id;


-- ============================================================================
-- VERIFICA: a combinacao valida do usuario e a que de fato esta montada nas R32?
-- Usa a tabela 'annexc' NUMERADA (combo_no). Duas perguntas de uma vez:
--   1) O CONJUNTO dos 8 terceiros forma uma combinacao valida do Anexo C?
--   2) Cada confronto esta com o terceiro CERTO no lugar certo (slot a slot)?
-- Um conjunto valido com confrontos trocados = 'VALIDA, MAS SLOTS DIVERGENTES'.
-- ============================================================================
WITH user_assign AS (
  SELECT
    bp.user_id,
    elem->>'id'     AS fixture_id,
    at.group_letter AS assigned_third_group        -- terceiro (visitante) que o motor pos
  FROM bracket_predictions bp
  CROSS JOIN LATERAL jsonb_array_elements(bp.payload->'bracket'->'fixtures') AS elem
  JOIN teams at ON at.competition_id = bp.competition_id AND at.code = elem->>'bottomTeamCode'
  WHERE elem->>'id' IN ('R32-74','R32-77','R32-79','R32-80','R32-81','R32-82','R32-85','R32-87')
    AND elem->>'topTeamCode'    IS NOT NULL
    AND elem->>'bottomTeamCode' IS NOT NULL
),
user_combo AS (
  SELECT
    user_id,
    count(*)                             AS confrontos,
    count(DISTINCT assigned_third_group) AS grupos_distintos,
    string_agg(assigned_third_group, '' ORDER BY assigned_third_group) AS combo_key
  FROM user_assign
  GROUP BY user_id
),
user_no AS (                                        -- no oficial da combinacao (NULL = invalida)
  SELECT uc.*, an.combo_no
  FROM user_combo uc
  LEFT JOIN (SELECT DISTINCT combo_no, combo_key FROM annexc) an
         ON an.combo_key = uc.combo_key
),
slots AS (                                          -- compara slot a slot contra a combo do usuario
  SELECT ua.user_id, un.combo_no, un.combo_key,
         ua.fixture_id, ua.assigned_third_group,
         ac.correct_third_group,
         (ua.assigned_third_group = ac.correct_third_group) AS slot_ok
  FROM user_assign ua
  JOIN user_no un ON un.user_id = ua.user_id
  LEFT JOIN annexc ac
         ON ac.combo_no   = un.combo_no
        AND ac.fixture_id = ua.fixture_id
)
SELECT
  u.id AS user_id, u.name, u.email,
  un.combo_no,
  un.combo_key,
  CASE
    WHEN un.confrontos       < 8 THEN 'INCOMPLETO (faltam confrontos)'
    WHEN un.grupos_distintos < 8 THEN 'INVALIDA (grupo de 3o repetido)'
    WHEN un.combo_no IS NULL     THEN 'INVALIDA (fora do Anexo C)'
    WHEN bool_and(s.slot_ok)     THEN 'OK (combinacao valida e confrontos corretos)'
    ELSE 'VALIDA, MAS SLOTS DIVERGENTES'
  END AS resultado,
  count(*) FILTER (WHERE NOT s.slot_ok) AS confrontos_errados,
  string_agg(
    CASE WHEN NOT s.slot_ok
         THEN s.fixture_id || ': pos 3' || s.assigned_third_group ||
              ' / Anexo C #' || un.combo_no || ' exige 3' || s.correct_third_group
    END, ', ' ORDER BY s.fixture_id)      AS divergencias
FROM slots s
JOIN user_no un ON un.user_id = s.user_id
JOIN users   u  ON u.id       = s.user_id
GROUP BY u.id, u.name, u.email, un.combo_no, un.combo_key,
         un.confrontos, un.grupos_distintos
ORDER BY resultado, u.name;
