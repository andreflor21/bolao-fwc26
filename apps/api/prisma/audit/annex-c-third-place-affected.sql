-- ============================================================================
-- CONSULTA 1 — QUEM FOI AFETADO (por usuario)
-- ============================================================================
WITH user_assign AS (
  -- Atribuicao que o MOTOR gravou no palpite de cada usuario, nos 8 confrontos
  -- de melhor-3o. winner_group = grupo do mandante (vencedor de grupo);
  -- assigned_third_group = grupo do 3o que o motor colocou ali.
  SELECT
    bp.user_id,
    elem->>'id'     AS fixture_id,
    ht.group_letter AS winner_group,
    at.group_letter AS assigned_third_group
  FROM bracket_predictions bp
  CROSS JOIN LATERAL jsonb_array_elements(bp.payload->'bracket'->'fixtures') AS elem
  JOIN teams ht ON ht.competition_id = bp.competition_id AND ht.code = elem->>'topTeamCode'
  JOIN teams at ON at.competition_id = bp.competition_id AND at.code = elem->>'bottomTeamCode'
  WHERE elem->>'id' IN ('R32-74','R32-77','R32-79','R32-80','R32-81','R32-82','R32-85','R32-87')
    AND elem->>'topTeamCode'    IS NOT NULL
    AND elem->>'bottomTeamCode' IS NOT NULL
),
user_key AS (
  -- Chave da combinacao prevista; so palpites com os 8 confrontos resolvidos.
  SELECT user_id,
         string_agg(assigned_third_group, '' ORDER BY assigned_third_group) AS combo_key
  FROM user_assign
  GROUP BY user_id
  HAVING count(*) = 8
),
diff AS (
  -- Confrontos em que o motor divergiu do Anexo C.
  SELECT ua.user_id, ua.fixture_id, ua.winner_group,
         ua.assigned_third_group, ac.correct_third_group
  FROM user_assign ua
  JOIN user_key uk ON uk.user_id = ua.user_id
  JOIN annexc   ac ON ac.combo_key = uk.combo_key
                  AND ac.winner_group = ua.winner_group
  WHERE ua.assigned_third_group <> ac.correct_third_group
)
SELECT
  u.id   AS user_id,
  u.name,
  u.email,
  count(*) AS confrontos_errados,
  string_agg(
    d.fixture_id || ': pos 3' || d.assigned_third_group ||
    ' / Anexo C exige 3' || d.correct_third_group,
    ', ' ORDER BY d.fixture_id)        AS divergencias,
  COALESCE(SUM(kgs.points), 0)         AS pontos_ko_nesses_confrontos
FROM diff d
JOIN users u ON u.id = d.user_id
LEFT JOIN knockout_guess_scores kgs
       ON kgs.user_id = d.user_id AND kgs.fixture_id = d.fixture_id
GROUP BY u.id, u.name, u.email
ORDER BY confrontos_errados DESC, u.name;


-- ============================================================================
-- CONSULTA 2 — O bracket OFICIAL (tabela matches) viola o Anexo C?
-- 1 linha por confronto; ok = false marca o slot fora do regulamento.
-- ============================================================================
WITH official_assign AS (
  SELECT m.bracket_fixture_id AS fixture_id,
         ht.group_letter      AS winner_group,
         at.group_letter      AS assigned_third_group
  FROM matches m
  JOIN teams ht ON ht.id = m.home_team_id
  JOIN teams at ON at.id = m.away_team_id
  WHERE m.bracket_fixture_id IN
        ('R32-74','R32-77','R32-79','R32-80','R32-81','R32-82','R32-85','R32-87')
),
official_key AS (
  SELECT string_agg(assigned_third_group, '' ORDER BY assigned_third_group) AS combo_key
  FROM official_assign
  HAVING count(*) = 8
)
SELECT oa.fixture_id, oa.winner_group,
       oa.assigned_third_group AS bracket_oficial_pos_3,
       ac.correct_third_group  AS anexo_c_exige_3,
       (oa.assigned_third_group = ac.correct_third_group) AS ok
FROM official_assign oa
CROSS JOIN official_key ok
JOIN annexc ac ON ac.combo_key = ok.combo_key AND ac.winner_group = oa.winner_group
ORDER BY oa.fixture_id;
