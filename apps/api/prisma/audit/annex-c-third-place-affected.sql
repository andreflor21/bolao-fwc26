-- ============================================================================
-- CONSULTA 0 — A combinacao dos 3os colocados e VALIDA? (por usuario)
-- Verifica se o conjunto de grupos atribuidos aos 8 confrontos de melhor-3o
-- corresponde a uma combinacao prevista no Anexo C, sem repetir grupo
-- e com os 8 confrontos resolvidos.
-- ============================================================================
WITH valid_combos AS (
  SELECT distinct combo_key,
         combo_no
  FROM annexc
),
user_assign AS (
  SELECT
    bp.user_id,
    elem->>'id'     AS fixture_id,
    at.group_letter AS assigned_third_group      -- grupo do 3o (visitante) atribuido pelo motor
  FROM bracket_predictions bp
  CROSS JOIN LATERAL jsonb_array_elements(bp.payload->'bracket'->'fixtures') AS elem
  JOIN teams at ON at.competition_id = bp.competition_id AND at.code = elem->>'bottomTeamCode'
  WHERE elem->>'id' IN ('R32-74','R32-77','R32-79','R32-80','R32-81','R32-82','R32-85','R32-87')
    AND elem->>'topTeamCode'    IS NOT NULL
    AND elem->>'bottomTeamCode' IS NOT NULL
),
user_combo AS (
  SELECT
    user_id,
    count(*)                             AS confrontos_resolvidos,
    count(DISTINCT assigned_third_group) AS grupos_distintos,
    string_agg(assigned_third_group, '' ORDER BY assigned_third_group) AS combo_key
  FROM user_assign
  GROUP BY user_id
)
SELECT
  u.id AS user_id, u.name, u.email,
  uc.combo_key,
  vc.combo_no,
  CASE
    WHEN uc.confrontos_resolvidos < 8 THEN 'INCOMPLETO (menos de 8 confrontos)'
    WHEN uc.grupos_distintos      < 8 THEN 'INVALIDA (grupo de 3o repetido)'
    WHEN vc.combo_key IS NULL         THEN 'INVALIDA (fora do Anexo C)'
    ELSE 'VALIDA'
  END AS status_combinacao
FROM user_combo uc
JOIN users u ON u.id = uc.user_id
LEFT JOIN valid_combos vc ON vc.combo_key = uc.combo_key
ORDER BY status_combinacao, u.name;


