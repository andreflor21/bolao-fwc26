-- combo_no <-> combo_key e 1:1. Numeracao oficial: #001 = EFGHIJKL, #495 =
-- ABCDEFGH. Use LEFT JOIN + combo_no IS NULL para tratar combinacoes invalidas
-- como invalidas (com JOIN direto elas somem do resultado e parecem corretas).
